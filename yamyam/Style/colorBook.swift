import UIKit

extension UIColor {
    
    @nonobjc class var sunfloweryellow: UIColor {
        return UIColor(red: 1.0, green: 206.0 / 255.0, blue: 0.0, alpha: 1.0)
    }
    
    @nonobjc class var warmGrey: UIColor {
        return UIColor(white: 162.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var greyishBrown: UIColor {
        return UIColor(white: 60.0 / 255.0, alpha: 1.0)
        return UIColor(red: 60.0 / 255.0, green: 60.0 / 255.0, blue: 60.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var palePurple: UIColor {
        return UIColor(red: 180.0 / 255.0, green: 179.0 / 255.0, blue: 184.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var goldenYellow: UIColor {
        return UIColor(red: 1.0, green: 195.0 / 255.0, blue: 34.0 / 255.0, alpha: 1.0)
    }
    
}

